package pers.cjw.fordemo;

public class ForDemo1 {
    public static void main(String[] args) {
        //for循环格式和c++一摸一样
        for (int i = 0; i <= 10; ++i) {
            System.out.println(i);
        }
        //总共循环多少次
        //如for (int i=a;i<b;++i)
        //小于号：b-a次
        //小于等于号：b-a+1次
        //可以通过简单预测输出结果的次数来判断到底用哪一个不等符号
        //用i++则是另外的情况

        //快捷输入：fori，然后修改循环次数
    }
}
